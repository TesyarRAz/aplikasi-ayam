package org.kelompokayam.tugas.datasource;

/**
 *
 * @author raz
 */
public class Session<T> {
    private String key;
    private T value;

    public Session(String key, T value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public T getValue() {
        return value;
    }

    public void setValue(T value) {
        this.value = value;
    }
    
    
}
